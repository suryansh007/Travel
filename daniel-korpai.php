          <div class="single-post">
            <div class="feature-img">
              <h2 style="font-style: italic; color: white;text-align: center">
                The Power of Design Side Projects
              </h2>

              <br />
              <br />

              <img class="img-fluid" src="images/interview/img2a.jpg" alt="" />
            </div>
            <div class="blog_details">
              <h2 style="color: white; text-align: center;"></h2>
              <br />
              <p class="excert" style="color: white; text-align: center;">
                Daniel Korpai is currently working as a product designer at
                InVision. In this interview, Daniel talks about how he solves
                his own problems and how he leveraged side projects to level
                up his design career.
              </p>
              <hr class="new3" />
              <div class="comments-area"></div>

              <p style="color: goldenrod; text-align: center;">
                Hey Daniel, nice to meet you. Let's start with a brief
                introduction of and how you got started in design?
              </p>
              <br />
              <p style="color: white; text-align: justify;">
                Hey Jan, it’s my pleasure to meet you. I’m Daniel Korpai and
                currently I’m working as a product designer at InVision to
                help designers, product managers, and engineers collaborate
                better together.
                <br />
                <br />
                Looking at my background, I have a bit different educational
                record than most designers, since I studied international
                business with marketing psychology. So we could say I had
                nothing to do with design during my educational years in
                university. However, I’ve been obsessed and passionate about
                gadgets since my early childhood, especially with phones and
                touch screen devices since the early days of PDAs (who can
                remember those, right?).
                <br />
                <br />
                As I grew up with these devices and read basically every
                review of them, using them was my second nature. But I
                remember when I first discovered the fact that my parents and
                other friends who weren’t into tech, couldn’t figure out how
                to use these devices at all. Probably this was the first time
                when I thought about user experience, way before the term UI
                and UX was born.
                <br />
                <br />
                After the introduction of the first iPhone and then later the
                App Store, I started writing reviews of iOS apps (more than
                300 reviews a year), mostly criticizing them for their
                difficult and confusing user interfaces. I believe criticizing
                without providing some alternative solutions is useless, so I
                started designing my own ideas and watched tutorials day and
                night. I still remember the summer afternoon when I first read
                about UI and UX after a Google search and basically, I
                disappeared that summer in my room trying to catch up with
                everything design related in a couple of weeks.
                <br />
                <br />
                Ever since then, I’m trying to combine design with business,
                marketing, and psychology to provide the best possible user
                experiences. ‍
              </p>
              <hr class="new3" />
              <div class="comments-area"></div>

              <p style="color: goldenrod; text-align: center;">
                For someone looking for new projects in their portfolio, how
                do you find or define interesting concepts to design around?
              </p>
              <br />
              <p style="color: white; text-align: center;">
                For selecting topics for my concepts, I usually try to choose
                a topic, which bothers me on a daily basis. This way, I’m
                fully invested in the design concept, since I’m solving my own
                problem, which makes the whole process easier, and I’m also
                more motivated to solve it.
                <br />
                <br />
                I think as designers we all recognize these problems around
                us, because we love complaining about them, but what if we
                don’t stop at complaining and we actually try to come up with
                alternative solutions for those concepts in addition to our
                complaints?
              </p>
              <hr class="new3" />
              <div class="quote-wrapper">
                <div class="quotes" style="text-align: center; color: red;">
                  "Try to use everything with an open mind and always ask
                  yourself if these things could be done better and if so, how
                  would that be possible and why is that important."
                </div>
              </div>

              <hr class="new3" />

              <div class="feature-img">
                <img class="img-fluid" src="images/interview/img2b.jpg" alt="" />
              </div>

              <br />
              <br />

              <p style="color: goldenrod; text-align: center;">
                Could you tell us about your process for finding solutions to
                these problems?
              </p>
              <br />
              <p style="color: white; text-align: jsutify;">
                Sure thing. Before doing any design related work, I always
                start with writing. First, I write down all the problems I’m
                trying to solve, the limitations, constraints, and goals of an
                ideal solution.
                <br />
                <br />
                I try to write down every single idea that comes to my mind
                around the problems I’m trying to solve. Sometimes days go by
                when I’m still just writing, and I’m always trying to capture
                my ideas as quickly as possible, no matter if it’s the middle
                of the night or I’m watching a movie.
                <br />
                <br />
                Capturing these ideas help me think more clearly about the
                problems I’m trying to solve and also provides a way to not
                get too excited about the first idea that comes to my mind
                when I’m sitting at my desk. I try to keep thinking about the
                problem when I’m going through my day and use different apps,
                browse dribbble, read design articles and case studies,
                because we never know when we stumble upon amazing ideas for
                potential solutions.
                <br />
                <br />
                When I sit down to work on the concept, I go through all my
                ideas, and I start drawing super simple wireframes using only
                rectangles and circles to explore as many solutions as
                possible. At this stage, I try to find even more possible
                solutions.
                <br />
                <br />
                When I feel like I found something interesting, I take that
                idea and polish it out in a higher fidelity version and then
                animate it to have a final prototype to see how it feels with
                interactions.
              </p>

              <hr class="new3" />

              <div class="comments-area"></div>

              <p style="color: goldenrod; text-align: center;">
                How have your conceptual projects helped you in your current
                position (or as a designer)?
              </p>
              <br />
              <p style="color: white; text-align: justify;">
                I believe working on concepts is one of the most beneficial
                exercises for designers, because they can create their own
                constraints and the solutions are not limited by external
                factors like developer resources, timelines, budgets, and
                client expectations. So through working on design concepts
                designers can showcase their whole potential of what they can
                create by themselves from scratch.
                <br />
                <br />
                Basically, all my clients from my freelancing years found me
                through my concepts, just as InVision reached out to me after
                seeing my design concepts I created with InVision Studio.
                <br />
                <br />
                Of course, working only on conceptual designs is not enough,
                but they’re excellent conversation starters and perfect for
                expressing your whole potential as a designer.
              </p>
              <hr class="new3" />
              <div class="comments-area"></div>

              <p style="color: goldenrod; text-align: center;">
                For someone looking to get their foot in the door, what’s your
                number one advice?
              </p>
              <br />
              <p style="color: white; text-align: center;">
                As design changes so quickly, there’s no way to stay
                up-to-date without continuously learning every single day,
                experimenting with new tools and trying out new techniques to
                further grow.
              </p>

              <hr class="new3" />
              <div class="quote-wrapper">
                <div class="quotes" style="text-align: center; color: red;">
                  "I like to follow a similar structure for all my case
                  studies, so I don’t have to reinvent the format every single
                  time, and I can focus on what truly matters, the content."
                </div>
              </div>
              <hr class="new3" />
              <p style="color: white; text-align: center;">
                <em>"I believe learning is the most important skill."</em>
                Learning by solving specific problems is probably the best
                way, because then you get to apply what you learn immediately
                through practice, so it won’t become only theoretical
                knowledge, but a useful skill that you can use in the future
                too.
              </p>
              <br />
            </div>
