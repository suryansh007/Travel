            <div class="feature-img">
              <h2 style="font-style: italic; color: white; text-align: center;">
                How to Break Into the World of UX Consulting
              </h2>

              <br />
              <br />

              <img class="img-fluid" src="images/interview/img1b.jpg" alt="" />
            </div>
            <br />
            <div class="blog_details">
              <h2 style="color: white; text-align: center;"></h2>

              <p class="excert" style="color: white; text-align: center;">
                He has taught at Hyper Island and consulted for companies such
                as IKEA, Spotify, First Principle, and Super Friendly. In this
                interview, Anton Sten shares his experience from running a UX
                consultancy business for more than ten years.
              </p>

              <hr class="new3" />

              <div class="comments-area"></div>

              <p style="color: goldenrod; text-align: center;">
                Hi Anton, nice to meet you. Let's start with a short
                background story. How did you get started in design?
              </p>
              <br />

              <p style="color: white; text-align: center;">
                Hey Jan! Well, if we go all the way back it started sometime
                in the mid-90’s. I was involved in the hardcore music scene
                back then (playing in bands, going to shows, even writing a
                fanzine). The entire scene was very DYI-focused so if you
                wanted to publish a fanzine, you needed to know basics of
                desktop publishing. I made a ton of different mix tapes too
                that I created custom covers for which ultimately got me into
                design. Fast forward a couple of years and I attended Hyper
                Island and studied Digital Media Design back in 2000. I then
                spent roughly ten years working for agencies and I’ve now run
                my own consultancy for a bit more than ten years! So here we
                are!
              </p>
              <hr class="new3" />

              <div class="comments-area"></div>

              <p style="color: goldenrod; text-align: center;">
                As a consultant, what core skills other than UX design do you
                think is important to develop?
              </p>
              <br />
              <p style="color: white; text-align: center;">
                Regardless of what you’re selling (UX, dev, design) - I think
                the core competency that you need as a consultant is
                communication skills. I think one of the keys to business
                success is being able to communicate your offering clearly and
                being able to communicate with clients in a trusting and
                respective way. I’ve worked with a ton of freelancers over the
                years that fail at this - they miss deadlines, they don’t give
                accurate estimates. Some have even forgotten to invoice me (so
                I had to remind them)! You can always outsource most of it if
                it’s not your cup of tea.
              </p>
              <hr class="new3" />
              <div class="quote-wrapper">
                <div class="quotes" style="text-align: center; color: red;">
                  "I think it makes sense to have some level of business
                  skills - after all, you’re running a business!"
                </div>
              </div>

              <br />
              <br />
              <div class="feature-img">
                <img class="img-fluid" src="images/interview/img1a.jpg" alt="" />
              </div>

              <br />
              <br />

              <p style="color: goldenrod; text-align: center;">
                How do you define sales, and how do you use it in your process
                for acquiring new clients?
              </p>
              <br />

              <p style="color: white; text-align: center;">
                Through trial and error, I’ve found that cold-calling (or even
                worse, cold-emailing) is very unlikely to lead to sales.
                Instead, I try to catch up with past clients on a regular
                basis. I’ve realised that I really don’t enjoy the sales
                process so I try to make it as informal as possible. The
                projects I work on are usually medium to large so it’s also
                unlikely that I’ll just email somehow and they are looking to
                build out a design system at that very point. I’ve also found
                that there’s one sales technique that works far better than
                anything else; word of mouth.
              </p>
              <br />
              <p style="color: white; text-align: center;">
                I try to keep all of my clients happy and by keeping in touch
                with them, they remember my name. It’s important to remember
                that we are all just humans and humans like to talk to each
                other. Rather than googling for a UX specialist, it’s far more
                likely that you’ll ask somehow you trust. If I can get them to
                drop my name in the hat at that point, 50% of the sales
                process is already done! It’s far easier to meet ends meet
                when someone’s asking for something you provide rather than
                you trying to convince them they need it.
              </p>

              <div class="comments-area"></div>
              <br />
              <hr class="new3" />
              <p style="color: goldenrod; text-align: center;">
                What do you think differentiate freelance vs. consultant UX
                designers?
              </p>
              <br />
              <p style="color: white; text-align: center;">
                I always try to avoid the term freelance because for some
                reason, I think it feels temporary. This is a matter of both
                self-and-public perception. I’ve found that saying, “I’m a
                freelancer” translates to some as, “I’m struggling and will
                take whatever shitty work you throw my way.” In other words,
                it sounds and feels desperate and poor. Instead I try to say
                that I own my own business or use the term consultant. To me,
                a consultant is someone who is more specialised and
                experienced.
              </p>
              <hr class="new3" />
              <div class="comments-area"></div>

              <p style="color: goldenrod; text-align: center;">
                What's your best advice for designers breaking into the world
                of consulting?
              </p>
              <br />
              <p style="color: white; text-align: center;">
                Wow, where to begin! There’s so much to freelancing and
                consulting that I even wrote a book about it :) I think it’s
                wise to ease into consulting if you currently have an
                employment. It will allow you to try things out and see if
                it’s for you or not. There’s this glorified idea about
                consulting, working from home in your underwear while earning
                a ton of cash! The truth is of course, very far from that
                image. There’s certainly pros and cons with everything and the
                only way to cope is to find something where the pros outweigh
                the cons.<br />For me personally, a mix of projects and the
                ability to control my own time are the most important things
                which I value more than a steady pay, colleagues and I guess,
                status. So transition into consulting and think about what
                you’re offering!
              </p>

              <hr class="new3" />
              <div class="quote-wrapper">
                <div class="quotes" style="text-align: center; color: red;">
                  "The clearer your sales pitch is, the easier it’ll be for
                  clients to understand what you’re selling."
                </div>
              </div>
              <hr class="new3" />
              <p style="color: white; text-align: center;">
                In the end, this will lead to you getting clients that are a
                better fit for your business. That said, you’re not going to
                make full-time pay until you go full-time. While I do think
                it’s a smart move to start small, perhaps doing work on the
                side before starting your own business, it’s important to
                remember that you’re not going to make full-time pay until you
                go full-time. This advice still stands after ten years, unless
                I work full-time it’s very unlikely I’ll make full-time pay.
              </p>
              <hr class="new3" />
